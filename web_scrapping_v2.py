    "# Estrategia 4: Sitemap XML → scraping individual (para Alkosto)\n",
    "# ---------------------------------------------------------------------------\n",
    "def obtener_urls_sitemap(base_url: str) -> list[str]:\n",
    "    \"\"\"Obtiene URLs de productos del sitemap.xml.\"\"\"\n",
    "    urls_producto = []\n",
    "\n",
    "    # Intentar sitemap principal\n",
    "    for sitemap_path in [\"/sitemap.xml\", \"/sitemap_products.xml\", \"/sitemap/sitemap.xml\"]:\n",
    "        url = f\"{base_url}{sitemap_path}\"\n",
    "        logger.info(\"  [Sitemap] Probando %s\", url)\n",
    "        html = hacer_request(url, headers={\"Accept\": \"application/xml, text/xml\"}, accept_html=True)\n",
    "        if not html or not isinstance(html, str):\n",
    "            continue\n",
    "\n",
    "        try:\n",
    "            root = ElementTree.fromstring(html)\n",
    "        except ElementTree.ParseError:\n",
    "            continue\n",
    "\n",
    "        ns = {\"s\": \"http://www.sitemaps.org/schemas/sitemap/0.9\"}\n",
    "\n",
    "        # Si es un sitemap index, buscar sub-sitemaps de productos\n",
    "        sitemap_urls = root.findall(\".//s:sitemap/s:loc\", ns)\n",
    "        if sitemap_urls:\n",
    "            for sm in sitemap_urls:\n",
    "                sm_url = sm.text\n",
    "                if not sm_url:\n",
    "                    continue\n",
    "                # Priorizar sitemaps que contengan \"product\"\n",
    "                if \"product\" in sm_url.lower() or \"supermercado\" in sm_url.lower():\n",
    "                    logger.info(\"  [Sitemap] Sub-sitemap de productos: %s\", sm_url)\n",
    "                    sub_html = hacer_request(\n",
    "                        sm_url,\n",
    "                        headers={\"Accept\": \"application/xml, text/xml\"},\n",
    "                        accept_html=True,\n",
    "                    )\n",
    "                    if sub_html and isinstance(sub_html, str):\n",
    "                        try:\n",
    "                            sub_root = ElementTree.fromstring(sub_html)\n",
    "                            for loc in sub_root.findall(\".//s:url/s:loc\", ns):\n",
    "                                if loc.text:\n",
    "                                    urls_producto.append(loc.text)\n",
    "                        except ElementTree.ParseError:\n",
    "                            pass\n",
    "\n",
    "                    if len(urls_producto) >= MAX_SITEMAP_PRODUCTS:\n",
    "                        break\n",
    "        else:\n",
    "            # Es un sitemap plano, extraer URLs directamente\n",
    "            for loc in root.findall(\".//s:url/s:loc\", ns):\n",
    "                if loc.text and \"/p/\" in loc.text.lower():\n",
    "                    urls_producto.append(loc.text)\n",
    "\n",
    "        if urls_producto:\n",
    "            break\n",
    "\n",
    "    return urls_producto[:MAX_SITEMAP_PRODUCTS]\n",
    "\n",
