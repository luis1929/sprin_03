    "def scrape_producto_html(url: str, tienda: str) -> Optional[dict]:\n",
    "    \"\"\"Extrae datos de un producto individual desde su página HTML.\"\"\"\n",
    "    html = hacer_request(url, headers={\"Accept\": \"text/html\"}, accept_html=True)\n",
    "    if not html or not isinstance(html, str):\n",
    "        return None\n",
    "\n",
    "    # Intentar JSON-LD (schema.org Product)\n",
    "    match = re.search(\n",
    "        r'<script\\s+type=\"application/ld\\+json\">(.*?)</script>',\n",
    "        html,\n",
    "        re.DOTALL,\n",
    "    )\n",
    "    if match:\n",
    "        try:\n",
    "            ld = json.loads(match.group(1))\n",
    "            # Puede ser una lista o un objeto\n",
    "            if isinstance(ld, list):\n",
    "                ld = next((x for x in ld if x.get(\"@type\") == \"Product\"), ld[0] if ld else {})\n",
    "\n",
    "            if ld.get(\"@type\") == \"Product\" or \"name\" in ld:\n",
    "                nombre = ld.get(\"name\", \"\")\n",
    "                marca = \"\"\n",
    "                if isinstance(ld.get(\"brand\"), dict):\n",
    "                    marca = ld[\"brand\"].get(\"name\", \"\")\n",
    "                elif isinstance(ld.get(\"brand\"), str):\n",
    "                    marca = ld[\"brand\"]\n",
    "\n",
    "                precio = None\n",
    "                precio_lista = None\n",
    "                offers = ld.get(\"offers\", {})\n",
    "                if isinstance(offers, list) and offers:\n",
    "                    offers = offers[0]\n",
    "                if isinstance(offers, dict):\n",
    "                    precio = offers.get(\"price\") or offers.get(\"lowPrice\")\n",
    "                    precio_lista = offers.get(\"highPrice\")\n",
    "\n",
    "                if nombre:\n",
    "                    return {\n",
    "                        \"tienda\": tienda,\n",
    "                        \"categoria\": \"\",\n",
    "                        \"nombre\": nombre,\n",
    "                        \"marca\": marca,\n",
    "                        \"precio\": precio,\n",
    "                        \"precio_lista\": precio_lista,\n",
    "                        \"producto_id\": ld.get(\"sku\", ld.get(\"productID\", \"\")),\n",
    "                        \"url_producto\": url,\n",
    "                    }\n",
    "        except (json.JSONDecodeError, StopIteration):\n",
    "            pass\n",
    "\n",
    "    # Intentar __NEXT_DATA__\n",
    "    match = re.search(\n",
    "        r'<script\\s+id=\"__NEXT_DATA__\"\\s+type=\"application/json\">(.*?)</script>',\n",
    "        html,\n",
    "        re.DOTALL,\n",
    "    )\n",
    "    if match:\n",
    "        try:\n",
    "            data = json.loads(match.group(1))\n",
    "            pp = data.get(\"props\", {}).get(\"pageProps\", {})\n",
    "            prod = pp.get(\"product\") or pp.get(\"data\", {}).get(\"product\", {})\n",
    "            if prod and isinstance(prod, dict):\n",
    "                item = extraer_producto_vtex(prod, tienda, \"\", url.rsplit(\"/\", 1)[0])\n",
    "                if item:\n",
    "                    item[\"url_producto\"] = url\n",
    "                    return item\n",
    "        except json.JSONDecodeError:\n",
    "            pass\n",
    "\n",
    "    return None\n",
