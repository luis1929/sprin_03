    "# ---------------------------------------------------------------------------\n",
    "def buscar_nextdata(base_url: str, path: str, tienda: str, categoria: str) -> list[dict]:\n",
    "    productos = []\n",
    "    url = f\"{base_url}{path}\"\n",
    "    logger.info(\"  [NextData] %s url='%s'\", tienda, url)\n",
    "\n",
    "    html = hacer_request(\n",
    "        url, headers={\"Accept\": \"text/html\"}, accept_206=True, accept_html=True\n",
    "    )\n",
    "    if not html or not isinstance(html, str):\n",
    "        return productos\n",
    "\n",
    "    match = re.search(\n",
    "        r'<script\\s+id=\"__NEXT_DATA__\"\\s+type=\"application/json\">(.*?)</script>',\n",
    "        html,\n",
    "        re.DOTALL,\n",
    "    )\n",
    "    if not match:\n",
    "        return productos\n",
    "\n",
    "    try:\n",
    "        next_data = json.loads(match.group(1))\n",
    "    except json.JSONDecodeError:\n",
    "        return productos\n",
    "\n",
    "    page_props = next_data.get(\"props\", {}).get(\"pageProps\", {})\n",
    "\n",
    "    def encontrar_productos(obj, depth=0):\n",
    "        if depth > 6:\n",
    "            return []\n",
    "        items = []\n",
    "        if isinstance(obj, dict):\n",
    "            for key, val in obj.items():\n",
    "                if isinstance(val, list) and len(val) > 0 and isinstance(val[0], dict):\n",
    "                    if any(\n",
    "                        k in val[0]\n",
    "                        for k in (\"productName\", \"productId\", \"nameComplete\")\n",
    "                    ):\n",
    "                        items.extend(val)\n",
    "                    elif \"product\" in key.lower():\n",
    "                        items.extend(val)\n",
    "                elif isinstance(val, dict):\n",
    "                    items.extend(encontrar_productos(val, depth + 1))\n",
    "        return items\n",
    "\n",
    "    raw = encontrar_productos(page_props)\n",
    "    for prod in raw:\n",
    "        item = extraer_producto_vtex(prod, tienda, categoria, base_url)\n",
    "        if item:\n",
    "            productos.append(item)\n",
    "\n",
    "    return productos\n",
